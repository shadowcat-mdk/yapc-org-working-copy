=item * Power Strips

At any conference you can never too many power strips. Ensure you have plenty of 
4-way or 6-way power strips around each room, running off long extension cables 
so they reach all around the room, not just at the front and the sides.

Remember that for most YAPCs, you will have attendees from the UK, mainland 
Europe, the United States and others, who all have different plug types. If 
you're unable to provide suitable adapters and some power strips with 
alternative sockets (previous organisers may be able to forward theirs), ensure 
you remind anyone arriving from abroad, that they can help you immensely if they 
can bring at least one power strip with them.

With all this power in the room, you will need to ensure that the ring main for 
the wall sockets can cope with the power drain. Most major venues will have more 
than one ring main in a room. In the UK a ring main usually operates on 32Amps 
at 240Volts, which can usually cope with a considerable number of laptops (IBM 
Thinkpads are typically about 80Watts) ... Watts = Volts x Amps for those who 
don't remember physics :)

