=head1 Running the Conference

=head2 Pre-Registration & Registration

It is always worthwhile opening up pre-registration the evening before the 
conference. Many people will arrive early, and the more conference material you 
can distribute before the day, the less time you'll be tied up registering 
people on the first morning. Your time will be precious on the first morning, so 
the less you have to do the better.

Whether you use the venue, a nearby bar or the conference accommodation, make 
sure you give yourself plenty of time to set up. Remember that the early 
arrivals will want to meet and greet other attendees, so if you're unable to 
arrange pre-registration in a environment that provides food and drink, you'll 
find that once attendees have registered, they will leave you and head elsewhere 
to socialise. You also need to eat, so ensure your team can rotate registration 
duties.

Registration during the conference should be at a specific point, that is pretty 
much directly in front of where the attendees will enter the venue. Failing that 
make posters to direct people to the registration desk

=head2 Lunches

If you can provide lunch at the venue, you'll save attendees the hassle of 
finding food. Universities and hotels often have onsite catering, so you may be 
able to provide a reasonable selection of dishes, including vegetarian and other 
specific dishes.

If you're not able to provide onsite catering and don't know the area around the 
venue well, start with Google Maps and create a map marked with key places such 
as pubs, bars, restaurants, drug stores and grocery stores. Then make a 
road-trip with your team and scout things out, with a camera. See if things are 
really there and are within walking distance. Take photos and create pages on 
your website to show people where places are and what they look like.

Try and take some time to try out some of the local pubs, bars, cafes and 
restaurants. If your attendees are having to fend for themselves during lunch, 
then you want to be able to recommend a few places for them to go, based on cost 
and cuisine. For example, in the UK you'll need to know where all your Real Ale 
pubs are :)

=head2 Staff - Runners & Timekeepers

Runners are those people who you can call on at the last minute, preferably with 
a car, to go and get stuff for you. Whether this is last minute items you've 
forgotten, or picking important speakers up from the airport/station or some 
other urgent need, they will be a blessing in disguise. Ensure their car parking 
is provided free, and that they can park as close to the venue as possible.

Timekeepers are vital to a smooth running conference. Plan to have large signs 
with the numbers 5 and 1 printed on them to indicate minutes remaining. Remember 
to synchronise the timekeepers watches. A speaker should be aiming to wrap up 
shortly at the 5 minutes marker and be pretty much done at the 1 minute marker. 
After the allotted time, timekeepers need to cutoff any further speaking. Bear 
in mind that a 5 minute gap between talks is short for attendees to move between 
rooms and for laptops to be switched over with the projectors.

Where possible employ partners and friends, who are not interested in the 
conference, to sit on the registration desk. While you may be there too, your 
attention may be otherwise engaged. Plus having someone who isn't interested in 
the conference watching the desk, means you can see some of the talks, although 
don't plan to see any talks yourself. If you get to see any, you will be 
extremely lucky.

=head2 Opening Address

You have a few responsibilities in your opening address:

=over

=item *

Thank your sponsors and everyone who helped. People gave money and time to
make your event happen. All they get is some recognition and the worst
thing you can do is forget them.

=item *

Explain what new-comers can expect from the conference. Explain that they can 
interact with anyone. This is their chance to talk to some cool people in a 
relatively informal environment. Explain about the wiki, IRC and the hallway 
track.

=back

=head2 Keynotes

You should have at least one keynote speaker, preferably someone very well known 
within the Perl community. Many of the big names in Perl, such as Larry, Damian, 
Chip and various members of TPF have all given keynotes at various YAPCs, so 
don't be shy in asking someone you would like to see along.

Keynotes can either open or close the conference, sometimes both, or even open 
each morning of the conference. The format is entirely up to you and depends on 
how many keynotes you've planned. If keynote addresses tie into your conference 
theme, then so much the better.

=head2 BOFs

BOFs are "Birds Of A Feather" sessions. Ad-hoc discussions created by and for 
attendees, so that they can have additional time to work on or talk about 
interesting topics to (usually) a small group of people. Make sure you allow 
time and room for these. Some will want to hold BOFs during the conference day, 
while others will want to find a bar or the hotel foyer in the evening.

=head2 Announcements

At the very least ensure you have time at the beginning of the day to make 
announcements. These will likely compromise of schedule changes, but can be 
anything that you need to get across to the attendees.

=head2 Closing Address

One important responsibility you must cover in your closing address:

  Thank your sponsors and everyone who helped.

You can't do this too much. People have given you their time and money to make 
this event happen, make sure you acknowledge them all.

=head1 AFTER THE CONFERENCE

=head2 Thank Yous

Write individual mails or letters to each sponsor, thanking them personally for 
their contribution. It never hurts to say thank you, and if you have created the 
right kind of rapport, then hopefully the sponsor will be willing to sponsor 
future events.

=head2 Accounts

Ensure your accounts are in order. While the money you raise is typically enough 
to cover all your costs, you still need to pay any outstanding bills. If you 
have incorporated then it would be wise to seek the advice of an accountant.

=head2 What Next?

Your next step is to perhaps have a rest. Or maybe even a night out for the 
organisers and staff. But you may also want to think about helping the 
organisers for next year. Read this document, is there anything you can add from 
your experience that isn't included here? Are there resources you used or 
created that can be passed on? Could you arrange a survey of the attendees, 
speakers and sponsors to see what kind of people you had attending? 

Statistics and surveys are useful, as they provide future organisers with some 
reasonably accurate data, which can help to interest sponsors who are aiming at 
a market that fits your conference demographic. Even compiling suggestions and 
feedback can be useful to know what areas weren't quite right, and could be 
perhaps improved. However, take some of them with a pinch of salt as you are 
never going to a perfect conference. There will always be a few who will 
complain about trivial things that often you had no control over. 

